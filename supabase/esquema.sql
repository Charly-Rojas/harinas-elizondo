create or replace function public.siguiente_secuencia_inspeccion(p_id_lote bigint)
returns text
language plpgsql
stable
set search_path = public
as $$
declare
  ultima_secuencia text;
begin
  select max(secuencia::text)
  into ultima_secuencia
  from public.inspecciones
  where id_lote = p_id_lote;

  if ultima_secuencia is null then
    return 'A';
  end if;

  if ultima_secuencia >= 'Z' then
    return null;
  end if;

  return chr(ascii(ultima_secuencia) + 1);
end;
$$;

