create or replace function public.usuario_actual_aprobado()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.perfiles
    where id = auth.uid()
      and aprobado = true
  );
$$;

create or replace function public.usuario_actual_es_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.perfiles
    where id = auth.uid()
      and aprobado = true
      and rol in ('superadmin', 'admin')
  );
$$;

create or replace function public.crear_perfil_usuario()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  es_superadmin_inicial boolean := lower(new.email) = 'superadmin@harinas-elizondo.local';
begin
  insert into public.perfiles (
    id,
    correo,
    nombre,
    rol,
    aprobado,
    aprobado_en
  )
  values (
    new.id,
    lower(new.email),
    coalesce(new.raw_user_meta_data ->> 'nombre', split_part(new.email, '@', 1)),
    case
      when es_superadmin_inicial then 'superadmin'::public.rol_usuario
      else 'operador'::public.rol_usuario
    end,
    es_superadmin_inicial,
    case
      when es_superadmin_inicial then timezone('utc', now())
      else null
    end
  )
  on conflict (id) do update
  set
    correo = excluded.correo,
    nombre = coalesce(public.perfiles.nombre, excluded.nombre);

  return new;
end;
$$;

