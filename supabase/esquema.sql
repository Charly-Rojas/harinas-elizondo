do $$
begin
  if not exists (
    select 1
    from pg_type
    where typnamespace = 'public'::regnamespace
      and typname = 'rol_usuario'
  ) then
    create type public.rol_usuario as enum ('superadmin', 'admin', 'operador');
  end if;
end
$$;

